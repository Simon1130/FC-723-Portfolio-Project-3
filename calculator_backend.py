class Calculater:
    
    def addition(x,y):
        return x + y
        
    def subtraction(x,y):
        return x - y
    
    def multiplication(x,y):
        return x * y
    
    def Division(x,y):
        return x / y
    
    def square(x):
        return x ** 2
    
    def square_root(x):
        return x ** 0.5
    
    def x_square(x,y): #y is the index
        return x ** y